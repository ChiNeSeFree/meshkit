function ascii_to_bin(ch)
{
 var lz = "z".charCodeAt(0),
     la = "a".charCodeAt(0),
     uz = "Z".charCodeAt(0),
     ua = "A".charCodeAt(0),
     ni = "9".charCodeAt(0),
     dt = ".".charCodeAt(0);

 if (ch >  lz) return 0;
 if (ch >= la) return (ch - la + 38);
 if (ch >  uz) return 0;
 if (ch >= ua) return (ch - ua + 12);
 if (ch >  ni) return 0;
 if (ch >= dt) return (ch - dt);
 return 0;
}


